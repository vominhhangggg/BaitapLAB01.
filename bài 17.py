a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
c = int(input("Nhập số nguyên c: "))
so_lon_nhat = max(a,b,c)
so_nho_nhat = min(a,b,c)
print("Số lơn nhất là: ", so_lon_nhat)              
print("Số nhỏ nhất là: ", so_nho_nhat)